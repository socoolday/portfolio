        				<p>
        					원로연극인들의 모임인 대학로 연극인 광장(회장 노경식)은 후배 연극인들을 격려하고 사기를 북돋자는 취지로 ‘올해의 연극인상’을 제정하고 12월 11일 운영위원회를 열어 제1회 수상자로 차유경 배우를 선정했다. 상금은 200만원이다.<br />
박정자 배우가 상금을 기탁한 ‘박정자 연기상’은 남윤호 배우가 첫 수상자로 뽑혔다. 상금은 100만원.<br />
2020년 11월 창립된 대연장은 이순재 신구 박정자 이승옥 이상일 유민영 김삼일(포항) 등 70세 이상 원로들로 구성된 연극인 친목 단체로 올해 연극인 자녀 장학금 지급에 이어 성금을 모아‘올해의 연극인상’을 제정, 올곧고 성실히 연극의 길을 걸어온 50대 이상 연극인 중에서 
올 한해 주목할 만한 활동을 보인 1인을 선정해 상금과 트로피를 수여한다.<br />
‘박정자 연기상’은 40대 이하 증 연기력이 탄탄하고 성실한 배우에게 주는 상으로, 상금과 트로피가 주어진다,
‘올해의 연극인상’ 첫 수상자로 선정된 차유경 배우(59)는 극단 실험극장 단원으로 1982년 로 데뷔해 40년간 무대 인생을 살아온 연기자다. 주요 출연작은 등이다. 백상예술대상 연극부문 신인상(1985), 한국여성연극협회 올빛상(2016), 포항바다국제연극제 여자 연기대상, 서울연극인대상 연기상(2018)등을 받았다.
정확한 발성과 깔끔한 연기로 정평을 얻은 차유경 배우는 지난 9월대학로 알과핵 소극장에서 공연된 (김숨 원작, 손정우 연출)에서 온 몸을 던져 위안부의 악몽(惡夢)을 연기해 “최후의 위안부 역을 맡 아 역사의 실상을 절절하게 증언했다”는 평을 받았다.<br />
이 작품에서 1인 2역을 맡은 차유경 배우는 그동안 다져온 무대 경륜과 특기인 정확한 발성, 마임을 보는 듯한 디테일한 몸 연기와 강약의 유연성으로 “기억의 고통 속으로 휘몰아치는 혼신의 열연”을 펼쳤 다.<br />
남윤호 배우(본명 유대식·37)는 영국 로열 할로웨이대학에서 영상을 전공한 후 미국 UCLA에서 연기 석사, RADA 영국왕립연극학교에서 연기 석사를 한 실력파로 특히 양정웅 연출과 작업을 해왔다. 
2012년로 데뷔한 그는 문체부 장관을 지낸 유인촌 배우의 아들로 아버지의 후광을 벗어나기 위해 남윤호란 예명으로 활동 중인데 에서 아버지와 한 무대에 서기도 했다.<br />
그간 출연작은 등이다.
남윤호 배우는 지난 7월 LG아트센터에서 공연된 셰익스피어 작, 양정웅 연출의 에서 ‘남윤호의 코리올라누스’라고 할만큼 타이틀롤을 탁월하게 해냈다는 평을 받았다.<br />
남윤호 배우는 각색에 참여하여 작품을 분석하고, 언어를 다듬고, 몸을 만들고, 운율을 살려 전달력이 뛰어난 화술을 연마하고, 연기진들과 앙상블을 맞추는 등 수년간 연극 한 편을 위해 전력투구했다. 
특히 번역극에서 보여왔던 특정 어투의 고정관념을 깨고 현재 우리가 쓰는 현대어로 셰익스피어 희곡의 대사와 독백을 정확한 발음, 리듬을 살린 화술로 구사해 주목을 받았다.
